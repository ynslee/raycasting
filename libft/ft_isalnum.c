#include "libft.h"

int	ft_isalnum(int c)
{
	int	res;

	if ((c >= 65 && c <= 90) || (c >= 97 && c <= 122)
		|| (c >= 48 && c <= 57))
		res = 1;
	else
		res = 0;
	return (res);
}
